// ── Live feed from the BDC scraper ───────────────────────────────────────────
// The BDC on Railway runs an hourly scraper that pulls the House of Carz
// ProMax feed into a Postgres table, and exposes a public endpoint:
//   GET https://lotlink-api-production.up.railway.app/api/inventory
//       ?dealership_id=<HoC UUID>
// This block fetches that feed on every page load, maps the API shape into
// the shape the rest of inventory.html / vehicle.html expect, and overwrites
// window.LOTLINK_INVENTORY if it succeeds. If the API is down, slow, or empty,
// we keep the hand-maintained 50-vehicle fallback above so the site never
// shows an empty lot.
//
// Pages that need fresh data should await window.LOTLINK_INVENTORY_READY
// before reading window.LOTLINK_INVENTORY.
(function attachLiveFeed() {
  // House of Carz dealership id in the BDC. When we onboard dealer #2 we'll
  // either union multiple dealerships or move this to a multi-tenant endpoint.
  var HOC_ID = 'cfff76a7-6385-4e2c-b4ee-63418198a564';
  var API = 'https://lotlink-api-production.up.railway.app/api/inventory?dealership_id=' + HOC_ID;
  // If network/API hiccups, give up fast and keep the static fallback.
  var TIMEOUT_MS = 6000;

  function mapVehicle(v, idx) {
    return {
      id: idx + 1,                            // stable-per-render numeric id for openModal(id)
      stock: v.stock_number || '',
      year: v.year || null,
      make: v.make || '',
      model: v.model || '',
      trim: v.trim || '',
      miles: (typeof v.mileage === 'number' ? v.mileage : (parseInt(v.mileage, 10) || null)),
      price: (typeof v.price === 'number' && v.price > 0 ? v.price : null),
      img: v.primary_photo || '',
      // pass-through in case vehicle detail pages want more:
      vin: v.vin || null,
      bodyStyle: v.body_style || null,
      exteriorColor: v.exterior_color || null,
      transmission: v.transmission || null,
      engine: v.engine || null,
      sourceUrl: v.source_url || null,
    };
  }

  function fetchWithTimeout(url, ms) {
    if (typeof AbortController === 'undefined') return fetch(url);
    var ctrl = new AbortController();
    var t = setTimeout(function () { ctrl.abort(); }, ms);
    return fetch(url, { signal: ctrl.signal }).finally(function () { clearTimeout(t); });
  }

  window.LOTLINK_INVENTORY_READY = fetchWithTimeout(API, TIMEOUT_MS)
    .then(function (r) { return r.ok ? r.json() : Promise.reject(new Error('HTTP ' + r.status)); })
    .then(function (payload) {
      var rows = (payload && payload.vehicles) || [];
      if (!rows.length) throw new Error('empty feed');
      var mapped = rows.map(mapVehicle).filter(function (v) { return v.stock && v.year && v.make; });
      if (!mapped.length) throw new Error('no usable rows');
      window.LOTLINK_INVENTORY = mapped;
      window.LOTLINK_INVENTORY_SOURCE = 'live';
      return mapped;
    })
    .catch(function (err) {
      // Keep the static fallback list. Log for debugging but don't alarm users.
      console.warn('[LotLink] Live inventory feed unavailable, using fallback:', err && err.message);
      window.LOTLINK_INVENTORY_SOURCE = 'fallback';
      return window.LOTLINK_INVENTORY;
    });
})();
